# Example 1: "Hello, World!"
print("Hello, World!")

# Example 2: Variables and Simple Arithmetic
a = 5
b = 3
sum = a + b

print(f"The sum of {a} and {b} is {sum}")

# Example 3: Conditional Statements
number = 10

if number > 0:
    print("The number is positive.")
elif number < 0:
    print("The number is negative.")
else:
    print("The number is zero.")

# Example 4: Loops
for i in range(1, 6):
    print(f"Iteration {i}")

# Example 5: Functions
def add(x, y):
    return x + y

result = add(5, 7)
print(f"The sum is {result}")
